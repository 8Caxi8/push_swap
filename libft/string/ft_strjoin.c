#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len;
	char	*a;

	if (!s1 && !s2)
		return (NULL);
	if (!s1)
		return (ft_strdup(s2));
	if (!s2)
		return (ft_strdup(s1));
	len = ft_strlen(s1) + ft_strlen(s2);
	a = malloc(len + 1);
	if (!a)
		return (NULL);
	ft_strlcpy(a, s1, len + 1);
	ft_strlcat(a, s2, len + 1);
	return (a);
}
